#ifndef AEGIS_ENCRYPTION_H
#define AEGIS_ENCRYPTION_H

#include <cstdint>
#include <string>
#include <vector>

class PDFEncryption {
public:
    PDFEncryption();
    ~PDFEncryption();

    bool encrypt(const uint8_t* input, size_t len, 
                 const char* password, std::vector<uint8_t>& output);
    bool decrypt(const uint8_t* input, size_t len, 
                 const char* password, std::vector<uint8_t>& output);
    
    void setAlgorithm(const char* algo); // "AES-128", "AES-256", "RC4"
    bool needsPassword() const;
    bool validatePassword(const char* password);

private:
    std::string algorithm;
    std::vector<uint8_t> encryptionKey;
    std::vector<uint8_t> salt;
    
    void deriveKey(const char* password);
    void generateIV(uint8_t* iv, size_t len);
};

#endif