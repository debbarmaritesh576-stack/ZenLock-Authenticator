#include "PDFRepair.h"
#include <cstdio>
#include <cstring>
#include <vector>
#include <algorithm>

PDFRepairEngine::PDFRepairEngine() {}
PDFRepairEngine::~PDFRepairEngine() {}

RepairResult PDFRepairEngine::scan(const char* filePath) {
    RepairResult result;
    result.success = true;
    result.objectsRecovered = 0;
    
    FILE* f = fopen(filePath, "rb");
    if (!f) {
        result.success = false;
        return result;
    }
    
    // Check header
    uint8_t header[8];
    fread(header, 1, 8, f);
    if (!checkHeader(header, 8)) {
        result.issuesFound.push_back({3, "Invalid or missing PDF header", 0});
        result.success = false;
    }
    
    // Get file size
    fseek(f, 0, SEEK_END);
    size_t fileSize = ftell(f);
    
    // Check for %%EOF marker
    fseek(f, fileSize > 1024 ? fileSize - 1024 : 0, SEEK_SET);
    uint8_t tail[1024];
    size_t tailSize = fread(tail, 1, 1024, f);
    
    bool foundEOF = false;
    for (size_t i = 0; i < tailSize - 5; i++) {
        if (memcmp(tail + i, "%%EOF", 5) == 0) {
            foundEOF = true;
            break;
        }
    }
    if (!foundEOF) {
        result.issuesFound.push_back({2, "Missing %%EOF marker", fileSize});
    }
    
    // Count objects
    fseek(f, 0, SEEK_SET);
    uint8_t* fullData = new uint8_t[fileSize];
    fread(fullData, 1, fileSize, f);
    
    std::vector<uint64_t> offsets;
    int objectsFound = findObjects(fullData, fileSize, offsets);
    
    if (objectsFound < 2) {
        result.issuesFound.push_back({3, "No recoverable objects found", 0});
    }
    
    result.objectsRecovered = objectsFound;
    delete[] fullData;
    fclose(f);
    
    return result;
}

RepairResult PDFRepairEngine::repair(const char* inputPath, const char* outputPath) {
    RepairResult result;
    FILE* input = fopen(inputPath, "rb");
    if (!input) {
        result.success = false;
        return result;
    }
    
    FILE* output = fopen(outputPath, "wb");
    if (!output) {
        fclose(input);
        result.success = false;
        return result;
    }
    
    // Write header
    fprintf(output, "%%PDF-1.7\n%%Repaired by Aegis PDF Engine\n");
    
    // Recover and write objects
    bool recovered = recoverObjects(inputPath, output);
    if (!recovered) {
        result.issuesFixed.push_back({2, "Partial recovery only", 0});
    }
    
    // Fix cross reference table
    fixCrossReference(inputPath, output);
    
    result.success = true;
    result.issuesFixed.push_back({1, "Document repaired successfully", 0});
    
    fclose(input);
    fclose(output);
    return result;
}

bool PDFRepairEngine::checkHeader(const uint8_t* data, size_t len) {
    return (len >= 5 && memcmp(data, "%PDF-", 5) == 0);
}

int PDFRepairEngine::findObjects(const uint8_t* data, size_t len, std::vector<uint64_t>& offsets) {
    int count = 0;
    for (size_t i = 0; i < len - 10; i++) {
        // Look for "obj" patterns
        if (data[i] >= '0' && data[i] <= '9') {
            // Check if followed by space+number+space+"obj"
            const char* ptr = (const char*)(data + i);
            long objNum = strtol(ptr, nullptr, 10);
            if (objNum > 0) {
                const char* objPtr = strstr(ptr, " obj");
                if (objPtr && (objPtr - ptr) < 20) {
                    offsets.push_back(i);
                    count++;
                }
            }
        }
    }
    return count;
}

bool PDFRepairEngine::recoverObjects(const char* inputPath, FILE* output) {
    FILE* input = fopen(inputPath, "rb");
    if (!input) return false;
    
    fseek(input, 0, SEEK_END);
    size_t fileSize = ftell(input);
    uint8_t* data = new uint8_t[fileSize];
    fseek(input, 0, SEEK_SET);
    fread(data, 1, fileSize, input);
    
    std::vector<uint64_t> offsets;
    findObjects(data, fileSize, offsets);
    
    int objNum = 1;
    for (uint64_t offset : offsets) {
        // Find "endobj"
        const char* ptr = (const char*)(data + offset);
        const char* endPtr = strstr(ptr, "endobj");
        
        if (endPtr) {
            size_t objLen = endPtr - ptr + 6;
            fprintf(output, "%d 0 obj\n", objNum);
            fwrite(ptr, 1, objLen, output);
            fprintf(output, "\n");
            objNum++;
        }
    }
    
    delete[] data;
    fclose(input);
    return objNum > 1;
}

bool PDFRepairEngine::fixCrossReference(const char* inputPath, FILE* output) {
    // Write basic xref table
    fprintf(output, "xref\n0 1\n0000000000 65535 f \n");
    fprintf(output, "trailer\n<< /Size 1 /Root 1 0 R >>\n");
    fprintf(output, "startxref\n0\n%%%%EOF\n");
    return true;
}