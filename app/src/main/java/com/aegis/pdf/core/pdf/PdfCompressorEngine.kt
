package com.aegis.pdf.core.pdf  
  
import android.content.Context  
import com.tom_roush.pdfbox.pdmodel.PDDocument  
import com.tom_roush.pdfbox.pdmodel.graphics.image.JPEGFactory  
import com.tom_roush.pdfbox.pdmodel.graphics.image.PDImageXObject  
import kotlinx.coroutines.Dispatchers  
import kotlinx.coroutines.withContext  
import java.io.File  
import javax.inject.Inject  
import javax.inject.Singleton  
  
@Singleton  
class PdfCompressorEngine @Inject constructor(  
    private val context: Context  
) {  
    // Native function declarations bridge matching cpp layer  
    private external fun compressPixelsNative(bitmap: Any, quality: Int): ByteArray  
  
    /**  
     * PDF ke andar ki heavy scanned images ko compress karke size drastically reduce karta hai.  
     */  
    suspend fun compressPdf(sourceFile: File, outputFile: File, quality: Float): Result<File> =   
        withContext(Dispatchers.IO) {  
            if (!sourceFile.exists()) return@withContext Result.failure(Exception("File missing"))  
  
            return@withContext try {  
                PDDocument.load(sourceFile).use { document ->  
                    // Iterate through all pages to look for resource objects (Images)  
                    for (page in document.pages) {  
                        val resources = page.resources  
                        for (name in resources.xObjectNames) {  
                            if (resources.isImageXObject(name)) {  
                                val image = resources.getXObject(name) as PDImageXObject  
                                val rawBitmap = image.image  
                                  
                                // Direct downsampling calculation via PDFBox streams factory  
                                // compression quality value ranges from 0.0 (max compression) to 1.0 (max quality)  
                                val compressedXObject = JPEGFactory.createFromImage(document, rawBitmap, quality)  
                                  
                                // Replace original heavy stream image object with optimized stream  
                                resources.put(name, compressedXObject)  
                            }  
                        }  
                    }  
                    outputFile.parentFile?.mkdirs()  
                    document.save(outputFile)  
                }  
                Result.success(outputFile)  
            } catch (e: Exception) {  
                Result.failure(e)  
            }  
        }  
}