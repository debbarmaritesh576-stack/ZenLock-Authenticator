package com.aegis.pdf.features.form

import android.content.Context
import android.net.Uri
import android.util.Log
import org.json.JSONArray
import org.json.JSONObject

class FormDataExporter(private val context: Context) {

    companion object {
        private const val TAG = "FormDataExporter"
    }

    /**
     * Export form data as JSON
     */
    fun exportToJson(fields: List<FormField>): ExportResult {
        return try {
            val json = JSONObject()
            val items = JSONArray()

            fields.forEach { field ->
                val item = JSONObject()
                item.put("id", field.id)
                item.put("name", field.name)
                item.put("type", field.type.name)
                item.put("value", field.value)
                items.put(item)
            }

            json.put("version", "1.0")
            json.put("exportDate", System.currentTimeMillis())
            json.put("fields", items)

            ExportResult.Success(json.toString(2))
        } catch (e: Exception) {
            Log.e(TAG, "JSON export failed", e)
            ExportResult.Error("Failed to export: ${e.message}")
        }
    }

    /**
     * Export as CSV with formula injection protection
     */
    fun exportToCsv(fields: List<FormField>): ExportResult {
        return try {
            val sb = StringBuilder()
            sb.appendLine("\"Field Name\",\"Type\",\"Value\"")  // Quoted headers

            fields.forEach { field ->
                val escapedName = escapeCsvField(field.name)
                val escapedType = escapeCsvField(field.type.name)
                val escapedValue = escapeCsvField(field.value)
                sb.appendLine("$escapedName,$escapedType,$escapedValue")
            }

            ExportResult.Success(sb.toString())
        } catch (e: Exception) {
            Log.e(TAG, "CSV export failed", e)
            ExportResult.Error("Failed to export CSV: ${e.message}")
        }
    }

    /**
     * Export as FDF with proper PDF string escaping
     */
    fun exportToFdf(fields: List<FormField>): ExportResult {
        return try {
            val parser = FormFieldParser()
            val sb = StringBuilder()
            sb.appendLine("%FDF-1.2")
            sb.appendLine("1 0 obj")
            sb.appendLine("<<")
            sb.appendLine("  /FDF <<")
            sb.appendLine("    /Fields [")

            fields.forEach { field ->
                val escapedName = parser.escapePdfString(field.name)
                val escapedValue = parser.escapePdfString(field.value)
                sb.appendLine("      <<")
                sb.appendLine("        /T ($escapedName)")
                sb.appendLine("        /V ($escapedValue)")
                sb.appendLine("      >>")
            }

            sb.appendLine("    ]")
            sb.appendLine("  >>")
            sb.appendLine(">>")
            sb.appendLine("endobj")
            sb.appendLine("trailer")
            sb.appendLine("<<")
            sb.appendLine("  /Root 1 0 R")
            sb.appendLine(">>")
            sb.appendLine("%%EOF")

            ExportResult.Success(sb.toString())
        } catch (e: Exception) {
            Log.e(TAG, "FDF export failed", e)
            ExportResult.Error("Failed to export FDF: ${e.message}")
        }
    }

    /**
     * Save to file with proper error handling
     */
    fun saveToFile(data: String, fileName: String, mimeType: String): ExportResult {
        return try {
            val resolver = context.contentResolver
            val values = android.content.ContentValues().apply {
                put(android.provider.MediaStore.Downloads.DISPLAY_NAME, fileName)
                put(android.provider.MediaStore.Downloads.MIME_TYPE, mimeType)
            }

            val uri = resolver.insert(
                android.provider.MediaStore.Downloads.EXTERNAL_CONTENT_URI,
                values
            ) ?: return ExportResult.Error("Failed to create file")

            resolver.openOutputStream(uri)?.use { stream ->
                stream.write(data.toByteArray(Charsets.UTF_8))
            } ?: return ExportResult.Error("Failed to open output stream")

            ExportResult.Success(uri.toString())
        } catch (e: SecurityException) {
            Log.e(TAG, "Permission denied writing file", e)
            ExportResult.Error("Storage permission required")
        } catch (e: Exception) {
            Log.e(TAG, "File save failed", e)
            ExportResult.Error("Save failed: ${e.message}")
        }
    }

    /**
     * Import from JSON with validation
     */
    fun importFromJson(jsonStr: String): ImportResult {
        return try {
            val json = JSONObject(jsonStr)
            val fields = mutableListOf<FormField>()
            val items = json.getJSONArray("fields")

            for (i in 0 until items.length()) {
                val item = items.getJSONObject(i)
                val typeStr = item.getString("type")

                val type = try {
                    FormFieldType.valueOf(typeStr)
                } catch (e: IllegalArgumentException) {
                    Log.w(TAG, "Unknown field type: $typeStr, defaulting to TEXT")
                    FormFieldType.TEXT
                }

                fields.add(
                    FormField(
                        id = item.optString("id", java.util.UUID.randomUUID().toString()),
                        name = item.optString("name", ""),
                        type = type,
                        value = item.optString("value", "")
                    )
                )
            }

            ImportResult.Success(fields)
        } catch (e: org.json.JSONException) {
            Log.e(TAG, "Invalid JSON format", e)
            ImportResult.Error("Invalid file format. Expected valid JSON.")
        } catch (e: Exception) {
            Log.e(TAG, "Import failed", e)
            ImportResult.Error("Import failed: ${e.message}")
        }
    }

    /**
     * Prevent CSV formula injection
     * Prefix dangerous characters with single quote
     */
    private fun escapeCsvField(field: String): String {
        val value = field.replace("\"", "\"\"") // Escape double quotes
        return when {
            value.startsWith("=") || value.startsWith("+") || 
            value.startsWith("-") || value.startsWith("@") -> "\"'$value\""
            value.contains(",") || value.contains("\"") || value.contains("\n") -> "\"$value\""
            else -> value
        }
    }

    sealed class ExportResult {
        data class Success(val dataOrUri: String) : ExportResult()
        data class Error(val message: String) : ExportResult()
    }

    sealed class ImportResult {
        data class Success(val fields: List<FormField>) : ImportResult()
        data class Error(val message: String) : ImportResult()
    }
}