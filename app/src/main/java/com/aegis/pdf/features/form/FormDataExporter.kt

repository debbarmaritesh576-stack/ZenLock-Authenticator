package com.aegis.pdf.features.form

import android.content.Context
import android.net.Uri
import org.json.JSONArray
import org.json.JSONObject

class FormDataExporter(private val context: Context) {

    fun exportToJson(fields: List<FormField>): String {
        val json = JSONObject()
        val items = JSONArray()
        
        fields.forEach { field ->
            val item = JSONObject()
            item.put("id", field.id)
            item.put("name", field.name)
            item.put("type", field.type.name)
            item.put("value", field.value)
            items.put(item)
        }
        
        json.put("version", "1.0")
        json.put("exportDate", System.currentTimeMillis())
        json.put("fields", items)
        
        return json.toString(2)
    }

    fun exportToCsv(fields: List<FormField>): String {
        val sb = StringBuilder()
        sb.appendLine("Field Name,Type,Value")
        fields.forEach { field ->
            sb.appendLine("${field.name},${field.type.name},${field.value}")
        }
        return sb.toString()
    }

    fun exportToFdf(fields: List<FormField>): String {
        val sb = StringBuilder()
        sb.appendLine("%FDF-1.2")
        sb.appendLine("1 0 obj")
        sb.appendLine("<< /FDF << /Fields [")
        
        fields.forEach { field ->
            sb.appendLine("  << /T (${field.name}) /V (${field.value}) >>")
        }
        
        sb.appendLine("] >> >>")
        sb.appendLine("endobj")
        sb.appendLine("trailer")
        sb.appendLine("<< /Root 1 0 R >>")
        sb.appendLine("%%EOF")
        
        return sb.toString()
    }

    fun saveToFile(data: String, fileName: String, mimeType: String): Uri? {
        return try {
            val resolver = context.contentResolver
            val values = android.content.ContentValues().apply {
                put(android.provider.MediaStore.Downloads.DISPLAY_NAME, fileName)
                put(android.provider.MediaStore.Downloads.MIME_TYPE, mimeType)
            }
            val uri = resolver.insert(
                android.provider.MediaStore.Downloads.EXTERNAL_CONTENT_URI,
                values
            )
            uri?.let {
                resolver.openOutputStream(it)?.use { stream ->
                    stream.write(data.toByteArray(Charsets.UTF_8))
                }
            }
            uri
        } catch (e: Exception) {
            null
        }
    }

    fun importFromJson(jsonStr: String): List<FormField> {
        val fields = mutableListOf<FormField>()
        try {
            val json = JSONObject(jsonStr)
            val items = json.getJSONArray("fields")
            for (i in 0 until items.length()) {
                val item = items.getJSONObject(i)
                fields.add(
                    FormField(
                        id = item.getString("id"),
                        name = item.getString("name"),
                        type = FormFieldType.valueOf(item.getString("type")),
                        value = item.optString("value", "")
                    )
                )
            }
        } catch (e: Exception) {
            e.printStackTrace()
        }
        return fields
    }
}