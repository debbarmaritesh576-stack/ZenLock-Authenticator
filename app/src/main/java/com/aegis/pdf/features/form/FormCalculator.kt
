package com.aegis.pdf.features.form

import android.util.Log
import java.util.UUID
import javax.inject.Inject
import javax.inject.Singleton

@Singleton
class FormCalculator @Inject constructor() {

    companion object {
        private const val TAG = "FormCalculator"
    }

    data class Calculation(
        val id: String = UUID.randomUUID().toString(),
        val targetFieldId: String,
        val expression: String,
        val sourceFields: List<String>
    )

    private val calculations = mutableMapOf<String, Calculation>()

    fun addCalculation(targetFieldId: String, expression: String, sourceFields: List<String>) {
        calculations[targetFieldId] = Calculation(
            targetFieldId = targetFieldId,
            expression = expression,
            sourceFields = sourceFields
        )
    }

    fun removeCalculation(targetFieldId: String) {
        calculations.remove(targetFieldId)
    }

    fun calculate(fields: Map<String, String>): Map<String, String> {
        val result = fields.toMutableMap()

        calculations.forEach { (targetId, calc) ->
            var expr = calc.expression

            // Replace field references with actual values
            calc.sourceFields.forEach { fieldId ->
                val value = fields[fieldId]?.toDoubleOrNull() ?: 0.0
                expr = expr.replace("{$fieldId}", value.toString())
            }

            val computed = safeEvaluate(expr)
            result[targetId] = formatResult(computed)
        }

        return result
    }

    fun autoCalculate(fieldId: String, value: String, allFields: Map<String, String>): Map<String, String> {
        val updated = allFields.toMutableMap()
        updated[fieldId] = value

        // Find calculations that depend on this field
        val dependent = calculations.filter { it.value.sourceFields.contains(fieldId) }
        dependent.forEach { (targetId, calc) ->
            val newValue = calculate(updated)[targetId]
            if (newValue != null) {
                updated[targetId] = newValue
            }
        }

        return updated
    }

    fun getCalculations(): List<Calculation> = calculations.values.toList()

    /**
     * Safe expression evaluator — no script engine, no eval injection
     * Supports: +, -, *, /, (, ), numbers, decimal
     */
    private fun safeEvaluate(expr: String): Double {
        return try {
            evaluateMathExpression(expr)
        } catch (e: Exception) {
            Log.w(TAG, "Expression evaluation failed: $expr", e)
            0.0
        }
    }

    /**
     * Simple math parser — handles +, -, *, / with parentheses
     */
    private fun evaluateMathExpression(expr: String): Double {
        val tokens = tokenize(expr)
        return parseExpression(tokens)
    }

    private fun tokenize(expr: String): List<String> {
        val tokens = mutableListOf<String>()
        var i = 0

        while (i < expr.length) {
            when {
                expr[i] == ' ' -> i++ // skip whitespace
                expr[i] in "+-*/()" -> {
                    tokens.add(expr[i].toString())
                    i++
                }
                expr[i].isDigit() || expr[i] == '.' -> {
                    val sb = StringBuilder()
                    while (i < expr.length && (expr[i].isDigit() || expr[i] == '.')) {
                        sb.append(expr[i])
                        i++
                    }
                    tokens.add(sb.toString())
                }
                else -> i++ // skip invalid characters
            }
        }
        return tokens
    }

    private var pos = 0
    private lateinit var tokens: List<String>

    private fun parseExpression(tok: List<String>): Double {
        tokens = tok
        pos = 0
        val result = parseAddition()
        if (pos != tokens.size) {
            Log.w(TAG, "Extra tokens in expression")
        }
        return result
    }

    private fun parseAddition(): Double {
        var left = parseMultiplication()
        while (pos < tokens.size) {
            when (tokens[pos]) {
                "+" -> { pos++; left += parseMultiplication() }
                "-" -> { pos++; left -= parseMultiplication() }
                else -> break
            }
        }
        return left
    }

    private fun parseMultiplication(): Double {
        var left = parsePrimary()
        while (pos < tokens.size) {
            when (tokens[pos]) {
                "*" -> { pos++; left *= parsePrimary() }
                "/" -> {
                    pos++
                    val right = parsePrimary()
                    if (right == 0.0) {
                        Log.w(TAG, "Division by zero")
                        left = 0.0
                    } else {
                        left /= right
                    }
                }
                else -> break
            }
        }
        return left
    }

    private fun parsePrimary(): Double {
        if (pos >= tokens.size) return 0.0

        return when {
            tokens[pos] == "(" -> {
                pos++ // skip '('
                val result = parseAddition()
                if (pos < tokens.size && tokens[pos] == ")") pos++ // skip ')'
                result
            }
            tokens[pos] == "-" -> {
                pos++ // unary minus
                -parsePrimary()
            }
            else -> {
                val value = tokens[pos].toDoubleOrNull() ?: 0.0
                pos++
                value
            }
        }
    }

    private fun formatResult(value: Double): String {
        return if (value == value.toLong().toDouble()) {
            value.toLong().toString()
        } else {
            "%.2f".format(value)
        }
    }

    fun clearAll() {
        calculations.clear()
    }
}