package com.aegis.pdf.features.form

import androidx.compose.foundation.layout.*
import androidx.compose.material3.*
import androidx.compose.runtime.*
import androidx.compose.ui.Modifier
import androidx.compose.ui.unit.dp

@Composable
fun TextFieldEditor(
    field: FormField,
    onValueChange: (String) -> Unit,
    modifier: Modifier = Modifier
) {
    var text by remember(field.id) { mutableStateOf(field.value) }
    var hasError by remember { mutableStateOf(false) }

    Column(modifier = modifier.fillMaxWidth().padding(8.dp)) {
        if (field.name.isNotEmpty()) {
            Text(
                text = field.name + if (field.isRequired) " *" else "",
                style = MaterialTheme.typography.labelLarge,
                color = if (hasError) MaterialTheme.colorScheme.error
                else MaterialTheme.colorScheme.onSurface
            )
            Spacer(modifier = Modifier.height(4.dp))
        }

        OutlinedTextField(
            value = text,
            onValueChange = { newValue ->
                if (field.maxLength == 0 || newValue.length <= field.maxLength) {
                    text = newValue
                    hasError = false
                    onValueChange(newValue)
                } else {
                    hasError = true
                }
            },
            modifier = Modifier.fillMaxWidth(),
            singleLine = !field.isMultiline,
            enabled = !field.isReadOnly,
            isError = hasError,
            supportingText = {
                if (field.tooltip.isNotEmpty()) {
                    Text(field.tooltip, style = MaterialTheme.typography.bodySmall)
                }
            },
            label = { Text(field.name) }
        )
    }
}